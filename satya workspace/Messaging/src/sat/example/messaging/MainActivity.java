package sat.example.messaging;

import android.os.Bundle;
import android.app.Activity;
import android.app.PendingIntent;
import android.content.Intent;
import android.telephony.SmsManager;
import android.view.Menu;
import android.view.View;
import android.widget.Button;
import android.widget.EditText;
import android.widget.TextView;
import android.widget.Toast;

public class MainActivity extends Activity {
	Button b;
	EditText e1,e2;
	String s1,s2;
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        b=(Button)findViewById(R.id.button1);
        e1=(EditText)findViewById(R.id.editTextnumber);
        e2=(EditText)findViewById(R.id.editTextMessage);
        b.setOnClickListener(new View.OnClickListener() {
			
			@Override
			public void onClick(View v) {
				// TODO Auto-generated method stub
				s1=e1.getText().toString();
				s2=e2.getText().toString();
				if(s1.length()>0 && s2.length() >0){
					sendSMS(s1,s2);
				}else{
					Toast.makeText(getApplicationContext(), "Fields canot be empty", Toast.LENGTH_SHORT).show();
				}
			}
		});
    }


    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.main, menu);
        return true;
    }
    
    public void sendSMS(String pn,String ms){
    	PendingIntent pi= PendingIntent.getActivity(getApplicationContext(), 0, new Intent(this,MainActivity.class), 0);
    	SmsManager sms = SmsManager.getDefault();
    	sms.sendTextMessage(pn, null, ms, pi, null);
    }
    
}




